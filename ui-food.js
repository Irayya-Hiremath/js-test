
function openNav() {
  document.getElementById("nav").style.width = "60%" ;
  document.getElementById("nav").style.border= "2px solid black";
  document.getElementById("nav").style.height = "100%" ;
  document.getElementById("nav").style.padding = "30px" ;


}

function closeNav() {
  document.getElementById("nav").style.width = "0";
  document.getElementById("nav").style.border= "0px";
  document.getElementById("nav").style.padding = "0px" ;


}
